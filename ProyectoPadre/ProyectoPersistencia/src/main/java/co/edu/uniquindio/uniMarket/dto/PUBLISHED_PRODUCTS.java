package co.edu.uniquindio.uniMarket.dto;

public class PUBLISHED_PRODUCTS {

	private String userID;
	private String email;
	private int totalRegistros;

	public PUBLISHED_PRODUCTS(String userID, String email, int totalRegistros) {
		super();
		this.userID = userID;
		this.email = email;
		this.totalRegistros = totalRegistros;
	}

	public String getUserID() {
		return userID;
	}

	public void setUserID(String userID) {
		this.userID = userID;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getTotalRegistros() {
		return totalRegistros;
	}

	public void setTotalRegistros(int totalRegistros) {
		this.totalRegistros = totalRegistros;
	}

	@Override
	public String toString() {
		return "PUBLISHED_PRODUCTS [userID=" + userID + ", email=" + email + ", totalRegistros=" + totalRegistros + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		result = prime * result + totalRegistros;
		result = prime * result + ((userID == null) ? 0 : userID.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PUBLISHED_PRODUCTS other = (PUBLISHED_PRODUCTS) obj;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		if (totalRegistros != other.totalRegistros)
			return false;
		if (userID == null) {
			if (other.userID != null)
				return false;
		} else if (!userID.equals(other.userID))
			return false;
		return true;
	}

}
